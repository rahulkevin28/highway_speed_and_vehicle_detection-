def estimate_speed(prev, curr, fps, track_id):
    prev_transformed = cv2.perspectiveTransform(np.array([[prev]], dtype=np.float32), H_matrix)[0][0]
    curr_transformed = cv2.perspectiveTransform(np.array([[curr]], dtype=np.float32), H_matrix)[0][0]
    dist_px = np.linalg.norm(np.array(curr_transformed) - np.array(prev_transformed))
    if dist_px > 100:
        return speed_dict.get(track_id, [0])[-1]  # use previous value if jump is too large
    time_diff = 1 / fps
    if time_diff == 0:
        return 0
    speed_mps = (dist_px * meters_per_pixel) / time_diff  # in m/s
    speed_kmh = speed_mps * 3.6
    if track_id in speed_dict:
        speed_dict[track_id].append(speed_kmh)
        if len(speed_dict[track_id]) > 10:
            speed_dict[track_id].pop(0)
        avg_speed = np.mean(speed_dict[track_id])
        if abs(speed_kmh - avg_speed) > 50:
            return avg_speed
        return avg_speed
    else:
        speed_dict[track_id] = [speed_kmh]
        return speed_kmh

