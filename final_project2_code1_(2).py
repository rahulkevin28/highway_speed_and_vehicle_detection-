# COCO Labels
COCO_INSTANCE_CATEGORY_NAMES = [
    '__background__', 'person', 'bicycle', 'car', 'motorcycle', 'airplane', 'bus', 'train',
    'truck', 'boat', 'traffic light', 'fire hydrant', 'stop sign', 'parking meter', 'bench'
]
ALLOWED_CLASSES = {"car", "truck", "bus", "motorcycle"}

# Confidence Thresholds
RCNN_CONF_THRESHOLD = 0.75
YOLO_CONF_THRESHOLD = 0.7
DETR_CONF_THRESHOLD = 0.6

# Accuracy Computation
def compute_accuracy(confidences):
    return np.mean(confidences) if confidences else 0.0

# Detection Functions
def detect_with_yolo(frame):
    results = yolo_model(frame)
    detected_boxes, confidences = [], []
    for result in results:
        for box, conf in zip(result.boxes.xyxy, result.boxes.conf):
            if conf > YOLO_CONF_THRESHOLD:
                detected_boxes.append(box.cpu().numpy().astype(int))
                confidences.append(conf.item())
    return detected_boxes, confidences

def detect_with_detr(image):
    inputs = detr_processor(images=image, return_tensors="pt")
    with torch.no_grad():
        outputs = detr_model(**inputs)
    target_sizes = torch.tensor([image.size[::-1]])
    results = detr_processor.post_process_object_detection(outputs, target_sizes=target_sizes, threshold=DETR_CONF_THRESHOLD)[0]

    boxes = [list(map(int, box.tolist())) for box in results["boxes"]]
    scores = results["scores"].tolist()
    return boxes, scores

def detect_with_faster_rcnn(frame):
    transform = transforms.ToTensor()
    image = transform(frame).unsqueeze(0)
    with torch.no_grad():
        predictions = faster_rcnn_model(image)

    detected_boxes, confidences = [], []
    if "boxes" not in predictions[0] or len(predictions[0]["boxes"]) == 0:
        return detected_boxes, confidences  # No detections, return empty lists

    for box, score, label in zip(predictions[0]['boxes'], predictions[0]['scores'], predictions[0]['labels']):
        label_idx = label.item()
        if 0 <= label_idx < len(COCO_INSTANCE_CATEGORY_NAMES):  # Check index validity
            class_name = COCO_INSTANCE_CATEGORY_NAMES[label_idx]
            if score > RCNN_CONF_THRESHOLD and class_name in ALLOWED_CLASSES:
                detected_boxes.append(box.cpu().numpy().astype(int))
                confidences.append(score.item())

    return detected_boxes, confidences

# Non-Maximum Suppression (NMS)
def apply_nms(boxes, scores, iou_threshold=0.5):
    if len(boxes) == 0:
        return [], []
    boxes_tensor = torch.tensor(boxes, dtype=torch.float32)
    scores_tensor = torch.tensor(scores, dtype=torch.float32)
    keep = ops.nms(boxes_tensor, scores_tensor, iou_threshold)
    return [boxes[i] for i in keep], [scores[i] for i in keep]

# Heatmap Generation
def generate_heatmap(frame, detected_boxes):
    heatmap = np.zeros((frame.shape[0], frame.shape[1]), dtype=np.float32)
    for box in detected_boxes:
        xmin, ymin, xmax, ymax = box
        heatmap[ymin:ymax, xmin:xmax] += 1

    heatmap = cv2.GaussianBlur(heatmap, (25, 25), 0)
    heatmap = (heatmap / np.max(heatmap) * 255).astype(np.uint8) if np.max(heatmap) > 0 else heatmap
    heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
    return cv2.addWeighted(frame, 0.6, heatmap, 0.4, 0)

# Detection Pipeline
def detect_traffic_insights(video_path, output_dir, display=False, detr_every_n_frames=2):
    os.makedirs(output_dir, exist_ok=True)
    cap = cv2.VideoCapture(video_path)
    frame_count = 0

    detr_confidences, rcnn_confidences, yolo_confidences = [], [], []
    vehicle_counts = []

    while cap.isOpened():
        ret, frame = cap.read()
        if not ret:
            break

        image_pil = Image.fromarray(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB))
        detected_boxes, scores = [], []

        # Use DETR every 2 frames
        if frame_count % detr_every_n_frames == 0:
            detr_boxes, detr_scores = detect_with_detr(image_pil)
            detected_boxes.extend(detr_boxes)
            scores.extend(detr_scores)
            detr_confidences.extend(detr_scores)

        # YOLO + Faster R-CNN every other frame
        else:
            yolo_boxes, yolo_scores = detect_with_yolo(frame)
            rcnn_boxes, rcnn_scores = detect_with_faster_rcnn(frame)

            detected_boxes.extend(yolo_boxes + rcnn_boxes)
            scores.extend(yolo_scores + rcnn_scores)

            yolo_confidences.extend(yolo_scores)
            rcnn_confidences.extend(rcnn_scores)

        # Apply NMS
        detected_boxes, scores = apply_nms(detected_boxes, scores)

        # Draw bounding boxes with unique labels
        vehicle_id = 1
        for (xmin, ymin, xmax, ymax) in detected_boxes:
            frame = cv2.rectangle(frame, (xmin, ymin), (xmax, ymax), (0, 255, 0), 2)
            label = f"{vehicle_id}"
            cv2.putText(frame, label, (xmin, ymin - 10), cv2.FONT_HERSHEY_SIMPLEX, 0.9, (0, 255, 0), 2)
            vehicle_id += 1

        # Generate heatmap
        frame = generate_heatmap(frame, detected_boxes)

        # Count vehicles safely
        vehicle_count = sum(1 for box in detected_boxes if len(box) == 4)
        vehicle_counts.append(vehicle_count)

        # Save frame
        output_frame_path = os.path.join(output_dir, f"frame_{frame_count:04d}.jpg")
        cv2.imwrite(output_frame_path, frame)
        frame_count += 1

        if display:
            cv2.imshow("Frame", frame)
            if cv2.waitKey(1) & 0xFF == ord('q'):
                break

    cap.release()

    # Compute final accuracies
    overall_acc = compute_accuracy(detr_confidences + rcnn_confidences + yolo_confidences)
    avg_vehicle_count = np.mean(vehicle_counts) if vehicle_counts else 0

    print(f"Overall Accuracy: {overall_acc:.2f}")
    print(f"Average Vehicle Count: {avg_vehicle_count:.2f}")
    print(f"Processed {frame_count} frames. Results saved in {output_dir}")

